# Tabs to skip — backup, legend, copy, and other non-data tabs
_SKIP_TAB_PATTERNS = (
    "legend", "backup", "copy of", "sheet", "overhead",
    "summary", "status_map", "host_inventory", "burndown",
    "site-hosts", "device_inventory", "connections", "site hosts", "hosts",
)


# Required column families for a valid cutsheet (R37)
_OPTIC_COLS = {"A-OPTIC", "A_OPTIC", "Z-OPTIC", "Z_OPTIC"}
_DEVICE_COLS = {
    "A-SIDE-DNS-NAME", "A-MODEL", "A-PORT",
    "Z-SIDE-DNS-NAME", "Z-MODEL", "Z-PORT",
    # Alternate naming
    "A SIDE DEVICE", "A MODEL", "A PORT",
}


def _verify_cutsheet_schema(df: pd.DataFrame, tab_name: str) -> None:
    """R37: Verify that the selected tab has optic AND device/port columns."""
    cols_upper = {c.upper().strip() for c in df.columns}
    has_optic = bool(cols_upper & {c.upper() for c in _OPTIC_COLS})
    has_device = bool(cols_upper & {c.upper() for c in _DEVICE_COLS})

    if not has_optic:
        raise ValueError(
            f"Tab '{tab_name}' has no optic columns (A-OPTIC / Z-OPTIC). "
            f"Columns found: {sorted(df.columns.tolist())[:10]}..."
        )
    if not has_device:
        raise ValueError(
            f"Tab '{tab_name}' has no device/port columns. "
            f"Columns found: {sorted(df.columns.tolist())[:10]}..."
        )


def _find_cutsheet_tab(filepath: str) -> str:
    """Find the best cutsheet tab in an xlsx file.

    Priority:
    1. CUTSHEET_CLEAN (preprocessed by Claude in Excel)
    2. CUTSHEET (standard name)
    3. First tab that isn't a skip-pattern match
    """
    xl = pd.ExcelFile(filepath, engine="calamine")
    names = xl.sheet_names
    xl.close()

    selected = None
    for name in names:
        if name.upper() == "CUTSHEET_CLEAN":
            selected = name
            break
    if selected is None:
        for name in names:
            if name.upper() == "CUTSHEET":
                selected = name
                break
    if selected is None:
        for name in names:
            if not any(pat in name.lower() for pat in _SKIP_TAB_PATTERNS):
                selected = name
                break
    if selected is None:
        selected = names[0]

    log.info("Selected tab: %r from %d available tabs", selected, len(names))
    return selected
