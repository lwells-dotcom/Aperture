def classify_status(raw_status: str, site_code: Optional[str] = None) -> str:
    """Map a raw STATUS string to a canonical value.

    Resolution order:
    1. Exact match against STATUS_MAP
    2. Case-insensitive match against _STATUS_MAP_LOWER
    3. Site-specific section header lookup via SITE_SECTION_HEADERS (if site_code given)
    4. Return UNKNOWN (caller decides what to do)
    """
    if not raw_status:
        return BLANK
    if raw_status in STATUS_MAP:
        return STATUS_MAP[raw_status]
    lower = raw_status.lower().strip()
    if not lower:
        return BLANK
    result = _STATUS_MAP_LOWER.get(lower, UNKNOWN)
    if result == UNKNOWN and site_code:
        site_headers = SITE_SECTION_HEADERS.get(site_code.upper(), {})
        if raw_status in site_headers:
            return SECTION_HEADER
    return result


def normalize_cutsheet_df(df: pd.DataFrame, site_code: Optional[str] = None) -> Tuple[pd.DataFrame, Dict[str, Any]]:
    """Normalize a raw cutsheet DataFrame.

    Adds STATUS_CANONICAL column, identifies section headers vs real data,
    and strips section-header rows.

    Returns:
        (clean_df, stats) where stats has keys:
        - rows_original: total rows in input
        - rows_stripped: section header rows removed
        - rows_blank: rows with BLANK status
        - rows_clean: rows in output
        - unknown_statuses: list of (value, count) for any UNKNOWN mappings
        - section_names: list of section header values found (for topology)
    """
    status_col = _find_col(df, _STATUS_CANDIDATES)
    if status_col is None:
        log.warning("No STATUS column found in DataFrame — skipping normalization")
        return df.copy(), {
            "rows_original": len(df),
            "rows_stripped": 0,
            "rows_blank": 0,
            "rows_clean": len(df),
            "unknown_statuses": [],
            "section_names": [],
        }

    result = df.copy()
    rows_original = len(result)

    # Vectorized: clean the status column
    raw_status = result[status_col].fillna("").astype(str).str.strip()
    # Replace "nan" strings from pandas NaN conversion
    raw_status = raw_status.where(raw_status.str.lower() != "nan", "")

    # Classify each status value — vectorized via .map() with fallback
    # Build a unique-value mapping to avoid per-row function calls
    unique_vals = raw_status.unique()
    val_to_canonical = {}
    for v in unique_vals:
        val_to_canonical[v] = classify_status(v, site_code=site_code)

    result["STATUS_CANONICAL"] = raw_status.map(val_to_canonical)

    # Heuristic fallback for UNKNOWN: if optic columns are both empty,
    # it's almost certainly a section header
    a_optic_col = _find_col(result, _A_OPTIC_CANDIDATES)
    z_optic_col = _find_col(result, _Z_OPTIC_CANDIDATES)

    if a_optic_col and z_optic_col:
        unknown_mask = result["STATUS_CANONICAL"] == UNKNOWN
        a_empty = result[a_optic_col].fillna("").astype(str).str.strip().eq("")
        z_empty = result[z_optic_col].fillna("").astype(str).str.strip().eq("")
        both_empty = a_empty & z_empty

        # R36: UNKNOWN + no optics + matches topology pattern = section header.
        # Without the topology check, notes rows and partial data rows get
        # wrongly promoted to section headers and stripped.
        candidates = unknown_mask & both_empty
        if candidates.any():
            topo_match = raw_status.str.contains(
                _TOPOLOGY_PATTERN, na=False
            )
            result.loc[candidates & topo_match, "STATUS_CANONICAL"] = SECTION_HEADER
            # Candidates that don't match topology stay UNKNOWN
        # UNKNOWN + has optics = stays UNKNOWN (flag for manual review)

    # Collect stats before stripping
    canonical_counts = result["STATUS_CANONICAL"].value_counts()
    rows_stripped = int(canonical_counts.get(SECTION_HEADER, 0))
    rows_blank = int(canonical_counts.get(BLANK, 0))

    # Collect unknown statuses for logging
    unknown_mask = result["STATUS_CANONICAL"] == UNKNOWN
    unknown_statuses = []
    if unknown_mask.any():
        unknown_vals = result.loc[unknown_mask, status_col].value_counts()
        unknown_statuses = [(val, int(cnt)) for val, cnt in unknown_vals.items()]
        for val, cnt in unknown_statuses:
            log.warning("UNKNOWN status value: %r (%d rows) — add to STATUS_MAP", val, cnt)

    # Collect section names for topology derivation
    section_mask = result["STATUS_CANONICAL"] == SECTION_HEADER
    section_names = sorted(result.loc[section_mask, status_col].unique().tolist())

    # Strip section header rows
    clean = result[result["STATUS_CANONICAL"] != SECTION_HEADER].copy()
    rows_clean = len(clean)

    log.info(
        "Preprocessor: %d original → %d clean (%d section headers stripped, %d blank, %d unknown)",
        rows_original, rows_clean, rows_stripped, rows_blank, len(unknown_statuses),
    )

    stats = {
        "rows_original": rows_original,
        "rows_stripped": rows_stripped,
        "rows_blank": rows_blank,
        "rows_clean": rows_clean,
        "unknown_statuses": unknown_statuses,
        "section_names": section_names,
    }

    return clean, stats


def count_optics_independently(df: pd.DataFrame) -> Dict[str, Dict[str, int]]:
    """Count A-OPTIC and Z-OPTIC as separate physical optics, with breakout deduplication.

    When a cable has a breakout panel (A-BREAKOUT LOC:CAB:RU is non-empty), the same
    A-side port fans out across multiple spreadsheet rows.  The A-optic is counted only
    once per unique A-LOC:CAB:RU + A-PORT combination.  The same deduplication applies
    to the Z-side when Z-BREAKOUT LOC:CAB:RU is non-empty.

    Returns:
        {
            "by_type": {
                "OSFP-800G-2DR4": {"a_side": 19748, "z_side": 8656, "total": 28404},
                ...
            },
            "totals": {"a_side": 24451, "z_side": 24416, "grand_total": 48867}
        }
    """
    a_optic_col    = _find_col(df, _A_OPTIC_CANDIDATES)
    z_optic_col    = _find_col(df, _Z_OPTIC_CANDIDATES)
    a_breakout_col = _find_col(df, _A_BREAKOUT_LOC_CANDIDATES)
    z_breakout_col = _find_col(df, _Z_BREAKOUT_LOC_CANDIDATES)
    a_loc_col      = _find_col(df, _A_LOC_CANDIDATES)
    a_port_col     = _find_col(df, _A_PORT_CANDIDATES)
    z_loc_col      = _find_col(df, _Z_LOC_CANDIDATES)
    z_port_col     = _find_col(df, _Z_PORT_CANDIDATES)

    by_type: Dict[str, Dict[str, int]] = defaultdict(lambda: {"a_side": 0, "z_side": 0, "total": 0})
    total_a = 0
    total_z = 0

    def _clean(col: str):
        vals = df[col].fillna("").astype(str).str.strip()
        return vals.where(vals.str.lower() != "nan", "")

    if a_optic_col:
        a_optic_vals = _clean(a_optic_col)
        if a_breakout_col and a_loc_col and a_port_col:
            a_breakout_vals = _clean(a_breakout_col)
            a_loc_vals      = _clean(a_loc_col)
            a_port_vals     = _clean(a_port_col)
            seen_a: set = set()
            for optic, breakout, loc, port in zip(
                a_optic_vals, a_breakout_vals, a_loc_vals, a_port_vals
            ):
                if not optic:
                    continue
                if breakout:
                    key = loc + "\x00" + port
                    if key in seen_a:
                        continue
                    seen_a.add(key)
                by_type[optic]["a_side"] += 1
                by_type[optic]["total"]  += 1
                total_a += 1
        else:
            # Breakout columns not found — fall back to counting every non-empty cell
            a_counts = a_optic_vals[a_optic_vals != ""].value_counts()
            for optic, count in a_counts.items():
                by_type[optic]["a_side"] += int(count)
                by_type[optic]["total"]  += int(count)
                total_a += int(count)

    if z_optic_col:
        z_optic_vals = _clean(z_optic_col)
        if z_breakout_col and z_loc_col and z_port_col:
            z_breakout_vals = _clean(z_breakout_col)
            z_loc_vals      = _clean(z_loc_col)
            z_port_vals     = _clean(z_port_col)
            seen_z: set = set()
            for optic, breakout, loc, port in zip(
                z_optic_vals, z_breakout_vals, z_loc_vals, z_port_vals
            ):
                if not optic:
                    continue
                if breakout:
                    key = loc + "\x00" + port
                    if key in seen_z:
                        continue
                    seen_z.add(key)
                by_type[optic]["z_side"] += 1
                by_type[optic]["total"]  += 1
                total_z += 1
        else:
            z_counts = z_optic_vals[z_optic_vals != ""].value_counts()
            for optic, count in z_counts.items():
                by_type[optic]["z_side"] += int(count)
                by_type[optic]["total"]  += int(count)
                total_z += int(count)

    return {
        "by_type": dict(by_type),
        "totals": {
            "a_side": total_a,
            "z_side": total_z,
            "grand_total": total_a + total_z,
        },
    }


def preprocess_upload(filepath: str, sheet_name: Optional[str] = None) -> Dict[str, Any]:
    """Full preprocessing pipeline for an uploaded cutsheet.

    1. Reads the xlsx and finds the CUTSHEET tab
    2. Runs normalize_cutsheet_df (adds STATUS_CANONICAL, strips section headers)
    3. Runs count_optics_independently on the clean data
    4. Returns everything downstream code needs

    Returns:
        {
            "clean_df": pd.DataFrame,       # section headers removed
            "optic_counts": dict,            # from count_optics_independently
            "rows_stripped": int,
            "rows_clean": int,
            "unknown_statuses": list,
            "section_names": list,
            "sheet_name": str,               # which tab was used
        }
    """
    path = Path(filepath)
    if not path.exists():
        raise FileNotFoundError(f"Cutsheet not found: {filepath}")

    # Find the right sheet
    if sheet_name:
        tab = sheet_name
    else:
        tab = _find_cutsheet_tab(filepath)

    log.info("Preprocessing %s (tab: %s)", path.name, tab)

    df = pd.read_excel(filepath, sheet_name=tab, engine="calamine")

    # R37: Verify the tab has the columns we need before processing
    _verify_cutsheet_schema(df, tab)

    clean_df, stats = normalize_cutsheet_df(df)
    optic_counts = count_optics_independently(clean_df)

    log.info(
        "Optic counts: A=%d  Z=%d  Total=%d  Types=%d",
        optic_counts["totals"]["a_side"],
        optic_counts["totals"]["z_side"],
        optic_counts["totals"]["grand_total"],
        len(optic_counts["by_type"]),
    )

    return {
        "clean_df": clean_df,
        "optic_counts": optic_counts,
        "rows_stripped": stats["rows_stripped"],
        "rows_clean": stats["rows_clean"],
        "unknown_statuses": stats["unknown_statuses"],
        "section_names": stats["section_names"],
        "sheet_name": tab,
    }


def format_optic_count_text(optic_counts: Dict[str, Any]) -> str:
    """Format optic counts as human-readable text for the upload response.

    Matches the style of Define_Optic_Count.count_all_files_gui output
    so the frontend doesn't need changes.
    """
    lines = []
    by_type = optic_counts["by_type"]
    totals = optic_counts["totals"]

    # Sort by total count descending
    sorted_types = sorted(by_type.items(), key=lambda x: x[1]["total"], reverse=True)

    for optic, counts in sorted_types:
        lines.append(f"{optic}: {counts['total']}  (A: {counts['a_side']}, Z: {counts['z_side']})")

    lines.append("")
    lines.append(f"Total A-side optics: {totals['a_side']}")
    lines.append(f"Total Z-side optics: {totals['z_side']}")
    lines.append(f"Grand total optics:  {totals['grand_total']}")
    lines.append("")
    lines.append(
        "Note: A-side optics on breakout fan-out rows are deduplicated by port key "
        "(one physical optic per A-LOC:CAB:RU + A-PORT). Z-side breakouts are "
        "deduplicated similarly."
    )

    return "\n".join(lines)


